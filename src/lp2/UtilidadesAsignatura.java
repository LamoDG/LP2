    private void botonOkAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonOkAltaActionPerformed

       
        boolean existeix = false;
        Iterator itArrayAsig = inici.ArrayLAsignaturas.iterator();

        try {

            if ("".equals(jTFNombreAlta.getText()) || "".equals(jFTFCapacidadAlta.getText()) || "-".equals((String) JCBTipoClaseAlta.getSelectedItem())) {

                JOptionPane.showMessageDialog(null,
                        "Hay campos vacios",
                        "Aletra",
                        JOptionPane.WARNING_MESSAGE);
            } else {

                String nombre = jTFNombreAlta.getText();
                String CapacidadString = jFTFCapacidadAlta.getText();
                int Capacidad = Integer.parseInt(CapacidadString);
                String identificadorString = jFTFCapacidadAlta.getText();
                int identificador = Integer.parseInt(identificadorString);
                String Tipo = (String) JCBTipoClaseAlta.getSelectedItem();

                if (!inici.ArrayLAsignaturas.isEmpty()) {

                   

//                    for(int x=0;x<inici.ArrayLAsignaturas.size();x++) {
                       while(itArrayAsig.hasNext()){
                        asignatura = (Asignatura) itArrayAsig.next();
                        if (asignatura.getNombre().equals(jTFNombreAlta.getText())) {
                            existeix = true;
                            JOptionPane.showMessageDialog(null,
                                    "Ya existe la asignatura",
                                    "Aletra",
                                    JOptionPane.WARNING_MESSAGE);
                        } 
                    }
                }
                if (!existeix) {
                    asignatura = new Asignatura(identificador, nombre, Tipo, Capacidad);
                    inici.ArrayLAsignaturas.add(asignatura);
                }

            }
        } catch (Exception ex) {
            Logger.getLogger(UtilidadesAsignatura.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonOkAltaActionPerformed

