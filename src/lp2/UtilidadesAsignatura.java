    private void botonBuscarModificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarModificacionActionPerformed

        Iterator itArrayAsig = inici.ArrayLAsignaturas.iterator();
        System.out.println("Hola hem fet s'iterador");

        if ("".equals(jTFNombreModificacionBuscar.getText())) {

            JOptionPane.showMessageDialog(null,
                    "No has escrito ningun identificador",
                    "Aletra",
                    JOptionPane.WARNING_MESSAGE);
        } else {

            JCBTipoClaseModificacion.setEnabled(true);

            botonOKModificacion.setEnabled(true);
            botonCancelarModificacion.setEnabled(true);

            jTFNombreModificacion.setEnabled(true);
            jTFIdentificacionModificacion.setEnabled(true);
            jFTFCapacidadModificacion.setEnabled(true);

            botonBuscarModificacion.setEnabled(false);

            if (!inici.ArrayLAsignaturas.isEmpty()) {
                asignatura = (Asignatura) itArrayAsig.next();
                System.out.println(" no esta buit");

                for(int x=0;x<inici.ArrayLAsignaturas.size();x++) {
               
                    if (asignatura.getNombre().equals(jTFNombreModificacionBuscar.getText())) {
                        jTFIdentificacionModificacion.setText(String.valueOf(asignatura.getIdentificacion()));
                        jTFNombreModificacion.setText(asignatura.getNombre());
                        jFTFCapacidadModificacion.setText(String.valueOf(asignatura.getCapacidad()));
                        jFTFCapacidadAlta.setText(String.valueOf(asignatura.getCapacidad()));
                        switch (asignatura.getTipo()) {
                            case "A":
                                JCBTipoClaseModificacion.setSelectedIndex(1);
                                break;
                            case "B":
                                JCBTipoClaseModificacion.setSelectedIndex(2);
                                break;
                            case "C":
                                JCBTipoClaseModificacion.setSelectedIndex(3);
                                break;
                            case "D":
                                JCBTipoClaseModificacion.setSelectedIndex(4);
                                break;
                            case "E":
                                JCBTipoClaseModificacion.setSelectedIndex(5);
                                break;
                            default:
                                JCBTipoClaseModificacion.setSelectedIndex(0);
                                break;

                        }
                     
                        System.out.println("trobat");
                    } else {
                        asignatura = (Asignatura) itArrayAsig.next();
                    }

                }
                System.out.println("fora bucle");
            } else {

            }
        }


    }//GEN-LAST:event_botonBuscarModificacionActionPerformed

